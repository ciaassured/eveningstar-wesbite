const fallbackVariant = eveningStarVariants[0];

function randomIndex(max: number) {
  if (window.crypto?.getRandomValues) {
    const value = new Uint32Array(1);
    window.crypto.getRandomValues(value);
    return value[0] % max;
  }

  return Math.floor(Math.random() * max);
}

export function pickInitialEveningStarVariant() {
  const requestedVariant = new URLSearchParams(window.location.search).get('variant');
  const explicitVariant = eveningStarVariants.find((variant) => variant.id === requestedVariant);

  if (explicitVariant) {
    return explicitVariant;
  }

  return eveningStarVariants[randomIndex(eveningStarVariants.length)] ?? fallbackVariant;
}
